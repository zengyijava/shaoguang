package com.montnets.emp.wymanage.vo;

public class GateShowVo
{
	// 通道名称
	private String	gatename;
	//通道账户名称
	private String	gateusername;
	//通道号码
	private String	gatenum;
	//短信签名
	private String	corpsign;



	public GateShowVo()
	{
	}



	public String getGatename()
	{
		return gatename;
	}



	public void setGatename(String gatename)
	{
		this.gatename = gatename;
	}



	public String getGateusername()
	{
		return gateusername;
	}



	public void setGateusername(String gateusername)
	{
		this.gateusername = gateusername;
	}



	public String getGatenum()
	{
		return gatenum;
	}



	public void setGatenum(String gatenum)
	{
		this.gatenum = gatenum;
	}



	public String getCorpsign()
	{
		return corpsign;
	}



	public void setCorpsign(String corpsign)
	{
		this.corpsign = corpsign;
	}

	
}
